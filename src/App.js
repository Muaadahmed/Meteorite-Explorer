import './App.css';
import axios from 'axios';
import {React, useEffect, useState, useCallback} from 'react';
import Input from './Input';
import Particles from 'react-particles';
import { loadFull } from "tsparticles";

//fetch meteorite landing data
//transfer data to a child component
function App() {
  const particlesInit = useCallback(async (engine) => {
    console.log(engine);
    // you can initiate the tsParticles instance (engine) here, adding custom shapes or presets
    // this loads the tsparticles package bundle, it's the easiest method for getting everything ready
    // starting from v2 you can add only the features you need reducing the bundle size
    await loadFull(engine);
  }, []);

  const particlesLoaded = useCallback(async (container) => {
    await console.log(container);
  }, []);
  const [meteoriteInfo, setMeteoriteInfo] = useState([]);
  useEffect(() => {
    axios.get('https://data.nasa.gov/resource/gh4g-9fh.json')
    .then((res) => {
      setMeteoriteInfo(res.data);
    })
    .catch((err) => {
      setMeteoriteInfo([]);
      console.log("API resquest failed: ", err.response.status)
    })
  }, []);
  return (
    <div className="App">
      <Input meteoriteSet={meteoriteInfo}/>
      <Particles
      id="tsparticles"
      init={particlesInit}
      loaded={particlesLoaded}
      options={{
        background: {
          color: {
            value: "#333",
          },
        },
        fpsLimit: 120,
        interactivity: {
          events: {
            onClick: {
              enable: false,
              mode: "push",
            },
            onHover: {
              enable: false,
              mode: "repulse",
            },
            resize: true,
          },
          modes: {
            push: {
              quantity: 4,
            },
            repulse: {
              distance: 200,
              duration: 0.4,
            },
          },
        },
        particles: {
          color: {
            value: "#fff",
          },
          links: {
            color: "#ffffff",
            distance: 150,
            enable: true,
            opacity: 0.5,
            width: 1,
          },
          collisions: {
            enable: true,
          },
          move: {
            directions: "none",
            enable: true,
            outModes: {
              default: "bounce",
            },
            random: false,
            speed: 6,
            straight: false,
          },
          number: {
            density: {
              enable: true,
              area: 800,
            },
            value: 80,
          },
          opacity: {
            value: 0.5,
          },
          shape: {
            type: "circle",
          },
          size: {
            value: { min: 1, max: 5 },
          },
        },
        detectRetina: true,
      }}
    />
    </div>
  );
}

export default App;
